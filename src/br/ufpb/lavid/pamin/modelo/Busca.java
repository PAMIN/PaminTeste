package br.ufpb.lavid.pamin.modelo;

public class Busca implements java.io.Serializable{
	
	private String nomebusca;
	private String tagbusca;
	private String dtinibusca;
	private String dtfimbusca;
	private String localbusca;
	private String endbusca;
	private String bairbusca;
	private String cidbusca;
	
	public String getNomebusca() {
		return nomebusca;
	}
	public void setNomebusca(String nomebusca) {
		this.nomebusca = nomebusca;
	}
	public String getTagbusca() {
		return tagbusca;
	}
	public void setTagbusca(String tagbusca) {
		this.tagbusca = tagbusca;
	}
	public String getDtinibusca() {
		return dtinibusca;
	}
	public void setDtinibusca(String dtinibusca) {
		this.dtinibusca = dtinibusca;
	}
	public String getDtfimbusca() {
		return dtfimbusca;
	}
	public void setDtfimbusca(String dtfimbusca) {
		this.dtfimbusca = dtfimbusca;
	}
	public String getLocalbusca() {
		return localbusca;
	}
	public void setLocalbusca(String localbusca) {
		this.localbusca = localbusca;
	}
	public String getEndbusca() {
		return endbusca;
	}
	public void setEndbusca(String endbusca) {
		this.endbusca = endbusca;
	}
	public String getBairbusca() {
		return bairbusca;
	}
	public void setBairbusca(String bairbusca) {
		this.bairbusca = bairbusca;
	}
	public String getCidbusca() {
		return cidbusca;
	}
	public void setCidbusca(String cidbusca) {
		this.cidbusca = cidbusca;
	}

}
